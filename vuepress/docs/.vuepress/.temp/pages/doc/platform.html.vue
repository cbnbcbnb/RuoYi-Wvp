<ol>
<li>名称：上级平台看到的下级平台名称;</li>
<li>本地IP：本地连接上级使用的具体哪个网卡;</li>
<li>SIP认证用户名：可以设置为与&quot;设备国标编号&quot;一致;</li>
<li>注册周期：间隔多久发起一次注册,单位秒;</li>
<li>心跳周期：间隔多久发送一次心跳,一般上级平台三次收不到心跳就会认为下级离线了, 所以建议{心跳周期}x3 &lt; 注册周期;</li>
<li>SDP发流IP：调用媒体节点发送视频流给上级时,使用的本地IP;</li>
<li>信令传输：信令传输模式,支持udp和TCP,没有特殊需求,默认UDP即可;</li>
<li>目录分组：上级发送&quot;CATALOG&quot;消息查询通道信息,每一条消息中携带几条通道信息,默认为1,增大该值,可以加快通道发送速度;</li>
