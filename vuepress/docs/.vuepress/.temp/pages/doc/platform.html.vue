<li>平台厂商：如果勾选&quot;其他选项/推送平台信息&quot;选项,会给上级推送平台信息,这里就是平台的平台厂商信息</li>
<li>平台型号：如果勾选&quot;其他选项/推送平台信息&quot;选项,会给上级推送平台信息,这里就是平台的平台型号信息</li>
<li>平台安装地址：如果勾选&quot;其他选项/推送平台信息&quot;选项,会给上级推送平台信息,这里就是平台的平台安装地址信息</li>
