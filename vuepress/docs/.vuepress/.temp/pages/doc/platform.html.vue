<p>国标28181不同平台之间支持两种连接方式，平级和上下级，系统目前支持向上级级联。</p>
<h2 id="添加上级平台" tabindex="-1"><a class="header-anchor" href="#添加上级平台"><span>添加上级平台</span></a></h2>
<p>在国标级联页面点击“添加”按钮，以推送到上级系统为例子，参看接入设备
