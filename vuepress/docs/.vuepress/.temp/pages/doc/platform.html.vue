<li>RTCP保活：在上级的流传输模式为UDP时,因为UDP的无状态特性,会无法知道上级是否在正常收流,启用RTCP保活时,就可以主动发送RTCP消息确认上级是否在正常收流, 异常情况下,可以下级主动停止发流;</li>
<li>消息通道：支持通过报警消息给上级级系统推送消息,消息内容由redis消息发送给系统,系统编辑成报警消息发送给上级;</li>
<li>主动推送通道：
系统模拟一条目录订阅信息,然后在共享通道变化时,发送CATAOLOG事件给上级,通知具体的通道变化,
目前支持的状态有: 状态改变事件 ON:上线,OFF:离线,VLOST:视频丢失,DEFECT:故障,ADD:增加,DEL:删除,UPDATE:更新;</li>
